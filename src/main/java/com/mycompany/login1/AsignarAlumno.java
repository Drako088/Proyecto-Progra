    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Curso cursoSeleccionado = null;
        Alumno alumnoSeleccionado = null;
        // Suponiendo que alumnoSeleccionado se obtiene del contexto de sesión del usuario logueado
        // Implementa esta función según tu contexto
        // Buscar el curso seleccionado
        for (Curso cl : Login1.cursos) {
            if (cl.NombreCurso.equals(jComboBox1.getSelectedItem().toString())) {
                cursoSeleccionado = cl;
                break;
            }
        }
        
        for (Alumno s : Login1.alumnos) {
            if (s.Nombre.equals(jComboBox2.getSelectedItem().toString())) {
                alumnoSeleccionado = s;
                break;
            }
        }
        
        if (cursoSeleccionado == null) {

            JOptionPane.showMessageDialog(null, "Debe seleccionar un curso válido");
            return;
        }

        // Verificar si el alumno puede inscribirse en más cursos (máximo 5)
        if (alumnoSeleccionado.Cursos.size() <= 5) {
            // Verificar si el curso puede aceptar más alumnos (máximo 10)
            if (cursoSeleccionado.Asignado.size() <= 10) {
                // Asignar alumno al curso y viceversa
                alumnoSeleccionado.Cursos.add(cursoSeleccionado);
                cursoSeleccionado.Asignado.add(alumnoSeleccionado);

                // Mostrar mensaje de confirmación
                JOptionPane.showMessageDialog(null, "Alumno " + alumnoSeleccionado.Usuario + " inscrito en el curso " + cursoSeleccionado.NombreCurso);
            } else {
                // Mostrar mensaje de error: curso lleno
                JOptionPane.showMessageDialog(null, "El curso " + cursoSeleccionado.NombreCurso + " está lleno");
            }
        } else {
            // Mostrar mensaje de error: alumno con máximo de cursos
            JOptionPane.showMessageDialog(null, "El alumno " + alumnoSeleccionado.Nombre + " ya tiene el máximo de cursos inscritos");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

