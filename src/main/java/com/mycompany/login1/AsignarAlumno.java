    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        JFileChooser archivo = new JFileChooser();
        int retorno = archivo.showDialog(this, "Cargar");
        if (retorno == JFileChooser.APPROVE_OPTION) {

            try {
                File f = new File(archivo.getSelectedFile().toPath().toString());
                FileReader fr = new FileReader(f);
                BufferedReader br = new BufferedReader(fr);

                String linea;
                while ((linea = br.readLine()) != null) {
                    String[] datos = linea.split("#");

                    for (Curso cl : Login1.cursos) {
                        if (cl.NombreCurso.equals(datos[1])) {

                            for (Alumno s : Login1.alumnos) {
                                if (s.Carnet.equals(datos[0])) {
                                    cl.Asignado.add(s);
                                }
                            }
                        }
                    }

                }
                fr.close();
                br.close();

            } catch (Exception s) {
                s.printStackTrace();
            }
        }


    }//GEN-LAST:event_jButton3ActionPerformed


