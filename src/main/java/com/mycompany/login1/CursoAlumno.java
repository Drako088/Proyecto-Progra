    private void actualizarTabla(){
        DefaultTableModel data = new DefaultTableModel (new String[]{"id","NombreCurso","Seccion","FechaInicio","FechaFin","HoraInicio","HoraFin", "Profesor"}, CursoSeleccionado.size());
        jTable1.setModel(data);
            
        int row=0;
        
        for(Curso cl : CursoSeleccionado){
            if(cl.Asignado.contains(Login1.alumnoLogeado)){
            jTable1.setValueAt(cl.id, row, 0);
            jTable1.setValueAt(cl.NombreCurso, row, 1);
            jTable1.setValueAt(cl.Seccion, row, 2);
            jTable1.setValueAt(cl.FechaInicio, row, 3);
            jTable1.setValueAt(cl.FechaFin, row, 4);
            jTable1.setValueAt(cl.HoraInicio, row, 5);
            jTable1.setValueAt(cl.HoraFin, row, 6);
            jTable1.setValueAt(cl.Profesor, row, 7);
            row++;
        }
    }}
    
    private void CargarTabla(){
        for (Curso cl : Login1.cursos){
            if(cl.Asignado.contains(Login1.alumnoLogeado)){
                CursoSeleccionado.add(cl);
            }
        }
        
    }
    
    
