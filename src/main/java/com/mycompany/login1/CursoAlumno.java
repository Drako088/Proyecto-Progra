    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String CursoNombre = jComboBox1.getSelectedItem().toString();
        
        
        for(Curso cl : Login1.cursos){
            if(cl.NombreCurso.equals(CursoNombre)){
                CursoSeleccion = cl;
                break;
            }
        }
        
        if(CursoSeleccion != null){
           CursoSeleccion.Asignado.add(Login1.alumnoLogeado);
           JOptionPane.showMessageDialog(null, "Curso Asignado");
           
        }
        jTable1.setModel(new DefaultTableModel());
        CargarTabla();
        actualizarTabla();
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        for(Curso cl : Login1.cursos){
            for(Curso cl1 : CursoSeleccionado){
                if(cl.NombreCurso.equals(cl1.NombreCurso)){
                    cl.Asignado.remove(Login1.alumnoLogeado);
                }
            }
        }
        
        for(Curso cl : CursoSeleccionado){
            cl.Asignado.remove(Login1.alumnoLogeado);
        }
        CargarTabla();
        actualizarTabla();
        JOptionPane.showMessageDialog(null, "Curso Eliminado");
    }//GEN-LAST:event_jButton3ActionPerformed


